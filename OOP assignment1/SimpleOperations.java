public class SimpleOperations{
    public static void main(String[] args){
        int num1 = 5;
        int num2 = 10;

        int sum = num1 + num2;
        int subtraction = num2 - num1;
        int multiplication = num1 * num2;
        int division = num2 / num1;

        System.out.println("The sum of two numbers is: " + sum);
        System.out.println("The substract of two numbers is: " + subtraction);
        System.out.println("The multiplication of two numbers is: " + multiplication);
        System.out.println("The division is equal to: " + division);

    }
}